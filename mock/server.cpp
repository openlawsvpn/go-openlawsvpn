/**
 * mock/server.cpp — minimal openvpn3-core based mock server
 *
 * Accepts one TLS+OpenVPN client connection, performs the HARD_RESET
 * handshake, and either:
 *   - completes the tunnel (for a valid-cert client), or
 *   - sends AUTH_FAILED,CRV1 (when MOCK_CRV1=1 env var is set).
 *
 * Every event is logged to stdout as one JSON object per line so the
 * Go test harness can parse structured output.
 *
 * Build: see mock/Dockerfile
 *
 * Reference: openvpn3-core server/manage.hpp, client/ovpncli.cpp
 */

